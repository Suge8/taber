export const messages = { en, zh } satisfies Record<Locale, SidepanelMessages>;

export function detectLocale(input: { stored?: string | null; manual?: boolean | null; browserLanguage?: string | null; navigatorLanguage?: string | null; navigatorLanguages?: readonly string[] | null } = {}): Locale {
  const stored = input.stored ?? readStoredLocale();
  const manual = input.manual ?? readStoredLocaleManual();
  if (manual && (stored === 'en' || stored === 'zh')) return stored;
  const navigatorLanguages = input.navigatorLanguages ?? (input.navigatorLanguage === undefined ? readNavigatorLanguages() : []);
  return detectLanguage([input.browserLanguage, ...navigatorLanguages, input.navigatorLanguage ?? readNavigatorLanguage()]);
}

export function persistLocale(locale: Locale) {
  if (typeof localStorage === 'undefined') return;
  localStorage.setItem(localeStorageKey, locale);
  localStorage.setItem(localeManualStorageKey, 'true');
}

export function formatTime(locale: Locale, value: number) {
  return new Intl.DateTimeFormat(locale, { hour: '2-digit', minute: '2-digit' }).format(new Date(value));
}

function detectLanguage(values: Array<string | null | undefined>) {
  for (const value of values) {
    const language = value?.toLowerCase();
    if (!language) continue;
    if (language.startsWith('zh')) return 'zh';
    if (language.startsWith('en')) return 'en';
  }
  return 'en';
}

function readStoredLocale() {
  return typeof localStorage === 'undefined' ? null : localStorage.getItem(localeStorageKey);
}

function readStoredLocaleManual() {
  return typeof localStorage !== 'undefined' && localStorage.getItem(localeManualStorageKey) === 'true';
}

function readNavigatorLanguages() {
  return typeof navigator !== 'undefined' && Array.isArray(navigator.languages) ? navigator.languages : [];
}

function readNavigatorLanguage() {
  return typeof navigator === 'undefined' ? 'en' : navigator.language;
}
